function createGrid(dimx: number, dimy: number, dist: number) : PointGraphEdgeData {

    const vertices: Point2D[] = [];
    const edges: Edge[] = [];

    for (let i = 0; i <= dimx; i++) {
        for (let j = 0; j <= dimy; j++) {
            vertices.push([i * dist, j * dist]);
            if (j < dimy) {
                edges.push([vertices.length -1, vertices.length]);
            }

            if (i < dimx) {
                edges.push([vertices.length -1, vertices.length + dimy])
            }
        }
    }
    
    return { vertices, edges };
}

function createWeirdoGrid(dimx: number, dimy: number, dist: number) : PointGraphEdgeData {

    const vertices: Point2D[] = [];
    const edges: Edge[] = [];

    for (let i = 0; i <= dimx; i++) {
        
        for (let j = 0; j <= dimy; j++) {

            const x = i * dist + ((Math.random() - 0.5) * dist * 0.9);
            const y = j * dist + ((Math.random() - 0.5) * dist * 0.9);

            vertices.push([x,y]);
            if (j < dimy) {
                edges.push([vertices.length -1, vertices.length]);
            }

            if (i < dimx) {
                edges.push([vertices.length -1, vertices.length + dimy])
            }
        }
    }
    
    return { vertices, edges };
}